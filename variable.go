package main

import "fmt"

func main() {
	var name string

	name = "Chasa Name"
	fmt.Println(name)

	name = "Chasa Name Again"
	fmt.Println(name)

	var friendName = "Farel"
	fmt.Println(friendName)

	var age = 30
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	country = "Amerika"
	fmt.Println(country)

	var (
		firstName = "Chasa"
		lastName  = "Name"
	)
	fmt.Println(firstName)
	fmt.Println(lastName)
}
